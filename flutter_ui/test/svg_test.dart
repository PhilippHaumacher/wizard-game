    assert(p is Traces);
    assert((p as Traces).parts.length == 18);
  });

  test("Parsing cubic path", () {
    var p = Trace.fromString("M 43.739075,0.80459084 30.278431,23.510008 26.414548,3.6203742 10.518686,27.74013 4.4988124,23.006065 C 4.3088064,22.834658 4.1091493,22.672499 3.8883804,22.526803 L 3.3870511,26.058225 0.1192992,49.35716 19.255645,38.78893 22.375312,37.062841 16.14113,32.161213 22.748061,22.137165 26.857443,43.269754 49.868051,4.4372995 Z");
    assert(p is Traces);
    var commands = (p as Traces).parts.length;
    assert(commands == 15);
  });


}
