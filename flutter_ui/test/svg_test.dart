import 'package:flutter_test/flutter_test.dart';
import 'package:flutter_ui/svg.dart';

void main() {
  test("Parsing coordinate", () {
    var p = Coordinate.fromString("-8.20549,8.20549");
    assert(p.x == -8.20549);
    assert(p.y == 8.20549);
  });

  test("Parsing arc path", () {
